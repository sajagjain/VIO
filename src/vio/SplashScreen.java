package vio;
import javax.swing.JProgressBar;
class ProgressIndicator extends Thread
{
JProgressBar pb;
public ProgressIndicator(JProgressBar pb)
{
this.pb=pb;
}
@Override
public void run()
{
try
{
    for (int i =pb.getMinimum(); i <pb.getMaximum(); i++) {
        pb.setValue(i);
        
        Thread.sleep(30);        
    }
    SplashScreen.lp.setVisible(false);
    new Login().setVisible(true);
}
catch(Exception ex){}
}
}
